    private void setScaledImage(Challenge challengeShow, JLabel label) {
        String ruta = challengeShow.getEnunciado(); // ejemplo: "/images/RETO_1_NIVEL_1_ENUNCUADO.png"
        java.net.URL url = getClass().getResource(ruta);

        if (url == null) {
            System.out.println("No se encontrÃ³ la imagen en: " + ruta);
            return;
        }

        ImageIcon icon = new ImageIcon(url);
        Image img = icon.getImage();

        int labelWidth = label.getWidth();
        int labelHeight = label.getHeight();

        if (labelWidth > 0 && labelHeight > 0) {
            int imgWidth = icon.getIconWidth();
            int imgHeight = icon.getIconHeight();

            // Escala proporcional
            double escalaX = (double) labelWidth / imgWidth;
            double escalaY = (double) labelHeight / imgHeight;
            double escala = Math.min(escalaX, escalaY);

            int nuevoAncho = (int) (imgWidth * escala);
            int nuevoAlto = (int) (imgHeight * escala);

            Image imgEscalada = img.getScaledInstance(nuevoAncho, nuevoAlto, Image.SCALE_SMOOTH);
            label.setIcon(new ImageIcon(imgEscalada));
        }
    }
    
    public  void printChallenge(){
        challengeShow = main.runGame();
        if(challengeShow != null){
            //jLabelDescription.setText(challengeShow.getEnunciado());
            setScaledImage(challengeShow, jLabelDescription);
            
            jLabelDescription.addComponentListener(new java.awt.event.ComponentAdapter() {
                @Override
                public void componentResized(java.awt.event.ComponentEvent evt) {
                    setScaledImage(challengeShow, jLabelDescription);
                }
            });



            
            String[] temp = challengeShow.getOpciones();
            
            jLabelOptionA.setText(temp[0]);
            jLabelOptionB.setText(temp[1]);
            jLabelOptionC.setText(temp[2]);
            jLabelOptionD.setText(temp[3]);
        
            String[] dataGame = main.dataGame();
            String namePlayer,challengerPass;
            int livePlayer;
            livePlayer = Integer.parseInt(dataGame[0]);
            namePlayer = dataGame[1];
            challengerPass  = dataGame[2];
        
            for (int index = 0; index < livePlayer; index++){
                jLabelLivesPlayer.setText(jLabelLivesPlayer.getText() + "\u2764" );
            }
        
            jLabelNamePlayer.setText(namePlayer); 
            jLabelChallenger.setText(challengerPass);
        }else{
            Start gameOver = new Start();
            gameOver.cMain(main);
            this.setVisible(false);
            gameOver.GameOver();
            gameOver.setVisible(true);

        }
        
    }

